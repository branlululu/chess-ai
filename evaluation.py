def evaluate_board(board):
    """Heuristic function to evaluate value at given board"""
    if board.is_checkmate():
        if board.turn: # White's turn: white is mated
            return -9999
        else: # Black's turn
            return 9999
    score = 0
    for pos in chess.SQUARES:
        if board.piece_at(pos):
            score += evaluate_position(board, pos)
    return score

def evaluate_position(board, pos):
    """Assigns score to each position by symbol and position"""
    piece = board.piece_at(pos)
    symbol = piece.symbol()
    is_black = symbol.islower()
    if is_black:
        row = 7 - pos // 8
    else:
        row = pos // 8
    # row = pos // 8
    col = pos % 8

    # upper case letters correspond to White, lower cases correspond to Black
    if symbol.lower() == 'p':
        score = 10 + int(10 * PAWN[row][col])
    elif symbol.lower() == 'n':
        score = 30 + int(10 * KNIGHT[row][col])
    elif symbol.lower() == 'b':
        score = 30 + int(10 * BISHOP[row][col])
    elif symbol.lower() == 'r':
        score = 50 + int(10 * ROOK[row][col])
    elif symbol.lower() == 'q':
        score = 90 + int(10 * QUEEN[row][col])
    elif symbol.lower() == 'k':
        score = 100 + int(10 * KING[row][col])
    
    return -score if is_black else score